function aeiLangueInitiale() {
  var memorisee = null;
  try { memorisee = localStorage.getItem("aei-langue"); } catch (e) {}
  if (memorisee && AEI_LANGUES.indexOf(memorisee) !== -1) return memorisee;
  var nav = (navigator.language || "fr").toLowerCase().slice(0, 2);
  return AEI_LANGUES.indexOf(nav) !== -1 ? nav : "fr";
}

function aeiAppliquerLangue(code) {
  var dico = AEI_I18N[code] || AEI_I18N.fr;
  document.documentElement.lang = code;
  document.querySelectorAll("[data-i18n]").forEach(function (el) {
    var cle = el.getAttribute("data-i18n");
    if (dico[cle] !== undefined) el.innerHTML = dico[cle];
  });
  var page = document.body.getAttribute("data-page");
  if (page && dico["title." + page]) document.title = dico["title." + page];
  var sel = document.getElementById("selecteur-langue");
  if (sel) sel.value = code;
  try { localStorage.setItem("aei-langue", code); } catch (e) {}
}

function aeiInjecterSelecteur() {
  var entete = document.querySelector(".entete-inner");
  if (!entete) return;
  var sel = document.createElement("select");
  sel.id = "selecteur-langue";
  sel.className = "selecteur-langue";
  sel.setAttribute("aria-label", "Langue / Language");
  AEI_LANGUES.forEach(function (code) {
    var opt = document.createElement("option");
    opt.value = code;
    opt.textContent = AEI_NOMS_LANGUES[code];
    sel.appendChild(opt);
  });
  sel.addEventListener("change", function () {
    aeiAppliquerLangue(sel.value);
  });
  var burger = document.getElementById("burger");
  if (burger) {
    entete.insertBefore(sel, burger);
  } else {
    entete.appendChild(sel);
  }
}

document.addEventListener("DOMContentLoaded", function () {
  aeiInjecterSelecteur();
  aeiAppliquerLangue(aeiLangueInitiale());
});
